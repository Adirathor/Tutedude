n=int(input("Enter a number: "))
import math
#square root
square_root = math.sqrt(n)
print("Square root: ", square_root)
#natural logarithm
logarithm = math.log(n)
print("logarithm: ", logarithm)
#Sine of the number in radians
m=float(n)
sine = math.sin(m)
print("Sine radians: ", sine)
